// Функция генерации SEO для комбинации категория + город
export function generateSEO(categorySlug: string, citySlug?: string) {
  const cat = categorySEO[categorySlug as keyof typeof categorySEO];
  if (!cat) return null;
  
  if (!citySlug) {
    // Только категория
    return {
      h1: cat.h1,
      h2: cat.h2,
      title: cat.title,
      description: cat.description,
      keywords: cat.keywords
    };
  }
  
  // Категория + город
  const city = citySEO[citySlug as keyof typeof citySEO];
  if (!city) {
    return {
      h1: cat.h1,
      h2: cat.h2,
      title: cat.title,
      description: cat.description,
      keywords: cat.keywords
    };
  }
  
  return {
    h1: cat.h1.replace('в России', city.h1),
    h2: city.h2,
    title: cat.title.replace('в России', city.titleSuffix),
    description: cat.description.replace('в России', city.descriptionSuffix),
    keywords: [...cat.keywords, citySlug]
  };
}